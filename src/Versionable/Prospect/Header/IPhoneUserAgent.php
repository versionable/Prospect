namespace Versionable\Prospect\Header;

class IPhoneUserAgent extends HeaderAbstract
{
    public function __construct($value = null)
    {
        $this->setName('User-Agent');
        $this->setValue('Apple iPhone OS 4: Mozilla/5.0 (iPhone; U; CPU iPhone OS 4_0 like Mac OS X; en-us) AppleWebKit/532.9 (KHTML, like Gecko) Version/4.0.5 Mobile/8A293 Safari/6531.22.7');
        parent::__construct($value);
    }
}
